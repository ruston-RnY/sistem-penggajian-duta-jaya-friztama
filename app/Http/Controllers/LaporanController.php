    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'periode_mulai' => 'required',
            'periode_akhir' => 'required',
        ]);

        $periode_mulai = $request->input('periode_mulai');
        $periode_akhir = $request->input('periode_akhir');
        
        $salaries = Salary::with('karyawan.absensi')->whereBetween('tanggal', [$periode_mulai, $periode_akhir])->get();
        // dd($salaries);
        return view('pages.laporan.cetak_laporan_gaji', compact('salaries', 'periode_mulai', 'periode_akhir'));
    }

